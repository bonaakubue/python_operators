#membership opeators
num = 3

#in operator
result =  num in [1,2,3,4,5]
print(result)

result = 'p' in 'apple'
print(result)

result = 'o' in 'apple'
print(result)

#not in operator
result =  num not in [1,2,3,4,5]
print(result)

result = 'p' not in 'apple'
print(result)

result = 'o' not in 'apple'
print(result)

