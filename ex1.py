#arithmetic operators

num1 = 20

num2 = 5

#addition operator
result =  num1 + num2
print(result)

#subtraction operator
result =  num1 - num2
print(result)

#multiplication
result =  num1 * num2
print(result)

#division
result =  num1 / num2
print(result)

#integer division
result =  num1 // num2
print(result)

#modulus operator
result =  num1 % num2
print(result)

#exponentiation operator
result =  num1 ** num2
print(result)