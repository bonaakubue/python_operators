#relational operators - ex4.py
num1 = 2
num2 = 3

#greater than
result =  num1 > num2
print(result)

#less than
result =  num1 < num2
print(result)

#greater or equal to
result =  num1 >= num2
print(result)

#less or equal to
result =  num1 <= num2
print(result)

#equality
result =  num1 == num2
print(result)

#not equality
result =  num1 != num2
print(result)